    public void setEditData(SinhVien s) {
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        String ngaySinh = format.format(s.getNgaySinh());
        //System.out.println(s.getGioiTinh().trim());
        txtMSSV.setText(s.getMSSV().trim());
        txtTen.setText(s.getTen().trim());
        txtNgaySinh.setText(ngaySinh.trim());
        txtQueQuan.setText(s.getQueQuan().trim());
        txtSDT.setText(s.getSDT().trim());
        txtDiaChi.setText(s.getDiaChi().trim());
        if(s.getGioiTinh().trim().equals("Nam")) {
            rbtn_nam.setSelected(true);
            rbtn_nu.setSelected(false);
        }else if(s.getGioiTinh().trim().equals("Nữ"))  {
            rbtn_nam.setSelected(false);
            rbtn_nu.setSelected(true);
        }
        
    }
