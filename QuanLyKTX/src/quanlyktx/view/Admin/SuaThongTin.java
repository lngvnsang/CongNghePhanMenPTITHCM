    private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMouseClicked
       String stringDate = txtNgaySinh.getText();

        try {
            String gioiTinh = rbtn_nam.isSelected() ? "Nam" : "Nữ";
            Date ngaySinh = new SimpleDateFormat("dd/MM/yyyy").parse(stringDate);

            SinhVien sinhVien
                    = new SinhVien(txtMSSV.getText(),
                            txtTen.getText(),
                            ngaySinh,
                            gioiTinh,
                            txtQueQuan.getText(),
                            txtSDT.getText(),
                            txtDiaChi.getText(),
                            txtMSSV.getText());
            home.updateSinhVien(sinhVien);
        } catch (ParseException ex) {
            Logger.getLogger(DangKyKTX.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_btn_saveMouseClicked

