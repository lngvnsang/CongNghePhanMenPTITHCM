    public void showDataDay(List<Day> data, DefaultTableModel model) {
        int i = 1;
        model.setRowCount(0);
        for (Day d : data) {
            model.addRow(new Object[]{
                i++,
                d.getMaDay().trim(),
                d.getTenDay(),
                d.getGioiTinh(),
                d.getTinhTrang(),});
        }
    }

    public void showDataPhong(List<Phong> data, DefaultTableModel model) {
        int i = 1;
        model.setRowCount(0);
        for (Phong p : data) {
            model.addRow(new Object[]{
                i++,
                p.getMaPhong(),
                p.getMaDay().trim(),
                p.getToiThieu(),
                p.getToiDa(),
                p.getMaLoaiPhong(),});
        }
    }
    

